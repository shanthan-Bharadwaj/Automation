
public class MethOver {
	
	public double rectperi(int a, int b) {
		System.out.println("Perimeter of a rectangle");
		double rp = 2*(a+b);
		return rp;
	}
	public double triperi(double l,double b,double h) {
	System.out.println("Perimeter of a triangle");
	double tp = l+b+h;
	return tp;
	}
	public double circleperi(double r) {
		System.out.println("Perimeter of a circle");
		double cp = 2*3.14*r;
		return cp;
	}

}
