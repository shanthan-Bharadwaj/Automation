package Vowels;

//We will pass 1 character to check whether it is a vowel or consonant

public class checKSingleCharIsVowelorConsonant {

	public static void main(String[] args) {
		char ch = 'B';
		if(ch=='A'||ch=='E'||ch=='I'||ch=='o'||ch=='U') {
			System.out.println(ch+ " "+ "is a vowel");
		}else {
			System.out.println(ch+ " "+ "is a consonant");
		}
				
		

	}

}
