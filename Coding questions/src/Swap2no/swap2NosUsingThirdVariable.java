package Swap2no;

//Swaaping 2 nos using third no.

public class swap2NosUsingThirdVariable {

	public static void main(String[] args) {
		int a=10,b=20,t;
		System.out.println("Before swap"+ " "+ "a is:"+a+" "+"b is:"+b);
		t=a;
		a=b;
		b=t;
		
		System.out.println("After swap"+ " "+ "a is:"+a+" "+"b is:"+b);

	}

}
